    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAceptarActionPerformed
    {//GEN-HEADEREND:event_botonAceptarActionPerformed
        int opcion;

        Integer nroSelecion = this.listaTiposDocumentosDisponibles.getSelectedIndex();
        if (nroSelecion != -1)
        {

            opcion = JOptionPane.showConfirmDialog(this, "¿Esta seguro que desa eliminar el documento?", "ELIMINACION", JOptionPane.YES_NO_OPTION);

            if (opcion == JOptionPane.YES_OPTION)
            {

                String seleccionado = this.listaTiposDocumentosDisponibles.getSelectedValue().toString();

                for (Iterator<GenericDto> it = misDocumentos.iterator(); it.hasNext();)
                {
                    GenericDto miDto = it.next();

                    if (seleccionado.equals(miDto.getString("nombre")))
                    {
                        try
                        {
                            Integer idTipoDocumento = miDto.getInt("idTipoDocumento");
                            
                            if (idTipoDocumento != null)
                            {
                                tipoDocumentoClient.remove(idTipoDocumento);
                                JOptionPane.showMessageDialog(this, "Se ha eliminado el documento seleccionado.", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                                inicializarFormulario();
                            } else
                            {
                                JOptionPane.showMessageDialog(this, "Error: No se pudo obtener el ID del documento", "ERROR", JOptionPane.ERROR_MESSAGE);
                            }
                        }
                        catch (IOException ex)
                        {
                            JOptionPane.showMessageDialog(this, "Error al comunicarse con el servidor: " + ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                            Logger.getLogger(EliminarDocumento.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        finally
                        {
                            DefaultListModel lista = new DefaultListModel();
                            listaTiposDocumentosDisponibles.setModel(lista);
                            botonAceptar.setEnabled(false);
                            this.listaTiposDocumentosDisponibles.updateUI();
                            this.inicializarFormulario();
                        }
                    }
                }
            }
        } else
        {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Documento");
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

