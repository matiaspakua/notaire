    private void inicializarFormulario()
    {
        misDocumentos = null;
        miDocumentoSeleccionado = null;
        
        try
        {
            misDocumentos = tipoDocumentoClient.findAll();

            if (misDocumentos != null && !misDocumentos.isEmpty())
            {
                DefaultListModel lista = new DefaultListModel();

                for (Iterator<GenericDto> it = misDocumentos.iterator(); it.hasNext();)
                {
                    GenericDto miDto = it.next();
                    String nombre = miDto.getString("nombre");
                    if (nombre != null)
                    {
                        lista.addElement(nombre);
                    }
                }

                this.listaTiposDocumentosDisponibles.setModel(lista);

            campoNombre.setEnabled(false);
            campoDiasVencimiento.setEnabled(false);
            campoEntrega.setEnabled(false);
            checkBoxVence.setEnabled(false);
            botonAceptar.setEnabled(false);

                campoNombre.setText("");
                campoDiasVencimiento.setText("");
                campoEntrega.setText("");
                checkBoxVence.setSelected(false);
            } else
            {
                JOptionPane.showMessageDialog(this, "No existen Tipos de Documento para eliminar.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                salir();
            }
        }
        catch (IOException ex)
        {
            JOptionPane.showMessageDialog(this, "Error al cargar documentos: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(EliminarDocumento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

