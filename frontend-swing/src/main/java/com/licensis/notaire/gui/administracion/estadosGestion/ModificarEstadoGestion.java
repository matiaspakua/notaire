    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAceptarActionPerformed
    {//GEN-HEADEREND:event_botonAceptarActionPerformed

        dtoSeleccionado.put("nombre", campoNombreEstadoGestion.getText());
        dtoSeleccionado.put("observaciones", campoObservacionesEstadoGestion.getText());

        try
        {
            estadoGestionClient.edit(dtoSeleccionado);
            JOptionPane.showMessageDialog(this, "Se ha modificado el estado de gestion.", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            salir();
        }
        catch (IOException ex)
        {
            JOptionPane.showMessageDialog(this, "Error al comunicarse con el servidor: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(Level.SEVERE, "Error al modificar estado de gesti贸n", ex);
            this.cargarListaEstadosDeGestion();
            this.limpiarFormulario();
        }

        limpiarFormulario();

    }//GEN-LAST:event_botonAceptarActionPerformed

