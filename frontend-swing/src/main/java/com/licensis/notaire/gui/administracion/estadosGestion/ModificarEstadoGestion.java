    private void botonSeleccionarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonSeleccionarActionPerformed
    {//GEN-HEADEREND:event_botonSeleccionarActionPerformed
        this.limpiarFormulario();
        try
        {
            String nombreEstado = listaEstadosDisponibles.getSelectedValue().toString();

            for (Iterator<GenericDto> it = miListaEstadosDeGestion.iterator(); it.hasNext();)
            {
                GenericDto dtoEstadoDeGestion = it.next();

                if (nombreEstado.equals(dtoEstadoDeGestion.getString("nombre")))
                {
                    cargarEstado(dtoEstadoDeGestion);
                    break;
                }
            }
        }
        catch (NullPointerException ex)
        {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ningun elemento", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botonSeleccionarActionPerformed

    private void cargarListaEstadosDeGestion()
    {
        try
        {
            miListaEstadosDeGestion = estadoGestionClient.findAll();

            if (miListaEstadosDeGestion != null && !miListaEstadosDeGestion.isEmpty())
            {
                //  limpiar lista de estados antes de cargar la nueva.
                listaEstadosDisponibles.removeAll();

                DefaultListModel lista = new DefaultListModel();

                for (Iterator<GenericDto> it = miListaEstadosDeGestion.iterator(); it.hasNext();)
                {
                    GenericDto dtoEstadoDeGestion = it.next();
                    String nombre = dtoEstadoDeGestion.getString("nombre");
                    if (nombre != null)
                    {
                        lista.addElement(nombre);
                    }
                }

                this.listaEstadosDisponibles.setModel(lista);
            } else
            {
                JOptionPane.showMessageDialog(this, "No existen estado para modificar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        }
        catch (IOException ex)
        {
            JOptionPane.showMessageDialog(this, "Error al cargar estados de gesti贸n: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(Level.SEVERE, "Error al cargar estados de gesti贸n", ex);
        }
    }

    private void cargarEstado(GenericDto unDto)
    {
        dtoSeleccionado = unDto;

        this.campoNombreEstadoGestion.setText(unDto.getString("nombre"));
        this.campoObservacionesEstadoGestion.setText(unDto.getString("observaciones"));
    }
