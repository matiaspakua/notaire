    private void botonAdministrarUsuariosActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAdministrarUsuariosActionPerformed
    {//GEN-HEADEREND:event_botonAdministrarUsuariosActionPerformed
        if (Usuarios.getEstadoFormulario() == Boolean.FALSE)
        {
            Principal.cargarFormulario(Usuarios.getInstancia());
            Principal.setVentanasActivas(Usuarios.getSubmenuUsuarios());
        }
        else
        {
            for (int i = 0; i < Principal.AreaTrabajo.getAllFrames().length; i++)
            {
                if (Principal.AreaTrabajo.getAllFrames()[i].equals(Usuarios.getInstancia()))
                {
                    Principal.cargarFormulario(Usuarios.getInstancia());
                    Principal.setVentanasActivas(Usuarios.getSubmenuUsuarios());
                }
            }
        }
    }//GEN-LAST:event_botonAdministrarUsuariosActionPerformed

