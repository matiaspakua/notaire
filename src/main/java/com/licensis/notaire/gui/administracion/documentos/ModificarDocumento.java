    private void botonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSeleccionarActionPerformed

        checkVenceDocumento.setSelected(true);
        selectorDiasVencimiento.setEnabled(true);
        selectorDiasVencimiento.setValue(0);

        Integer nroSeleccionado = listaDocumentosDisponibles.getAnchorSelectionIndex();

        if (nroSeleccionado == ConstantesGui.ERROR)
        {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de documento", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        else
        {

            String seleccionado = listaDocumentosDisponibles.getSelectedValue().toString();

            for (Iterator<DtoTipoDeDocumento> it = misDocumentos.iterator(); it.hasNext();)
            {
                DtoTipoDeDocumento miDto = it.next();

                this.activarCampos(miDto);

                if (miDto.getNombre().equals(seleccionado))
                {
                    campoNombreDocumento.setText(miDto.getNombre());

                    checkVenceDocumento.setSelected(miDto.isVence());

                    if (miDto.isVence())
                    {
                        selectorDiasVencimiento.setValue(miDto.getDiasVencimiento());
                    }

                    if (miDto.getQuienEntrega().equals("Cliente"))
                    {
                        comboQuienEntrega.setSelectedItem("Cliente");
                    }
                    else
                    {
                        comboQuienEntrega.setSelectedItem("Entidad Externa");
                    }

                    miDocumentoSeleccionado = miDto;

                    break;
                }
            }
        }


    }//GEN-LAST:event_botonSeleccionarActionPerformed

