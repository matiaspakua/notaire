    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed

        /*
         * Controlo que todos los campos sean correctos
         */
        String msj = AdministradorValidaciones.getInstancia().validarModificarDocumento(this);

        if (msj == "")
        {

            if (checkVenceDocumento.isSelected() == true)
            {
                miDocumentoSeleccionado.setVence(true);
                miDocumentoSeleccionado.setDiasVencimiento((Integer) selectorDiasVencimiento.getValue());
            }
            else
            {
                miDocumentoSeleccionado.setVence(false);
                miDocumentoSeleccionado.setDiasVencimiento(null);
            }

            miDocumentoSeleccionado.setQuienEntrega(comboQuienEntrega.getSelectedItem().toString());

            try
            {
                DtoFlag seModifico = miController.modificarTipoDeDocumento(miDocumentoSeleccionado);

                if (seModifico.getFlag())
                {
                    JOptionPane.showMessageDialog(this, "Se ha modificado el tipo de documento.", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                    limpiarFormulario();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "<HTML>ERROR: No se ha modificado el tipo de documento.<BR>Se ha insertado mal alguno de los campos.</HTML>", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
            catch (ClassModifiedException ex)
            {

                JOptionPane.showMessageDialog(this, "El Documento ha sido recientemente modificado por otro usuario.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                this.inicializarFormulario();

            }
            catch (ClassEliminatedException ex)
            {
                JOptionPane.showMessageDialog(this, "El Documento ha sido recientemente eliminado por otro usuario.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                this.inicializarFormulario();
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Error grave, Accion cancelada", "Error", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(ModificarDocumento.class.getName()).log(Level.SEVERE, null, ex);
                this.salir();
            }
            finally
            {
                listaDocumentosDisponibles.removeAll();
                inicializarFormulario();
            }

        }
        else
        {
            JOptionPane.showMessageDialog(this, msj);
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

