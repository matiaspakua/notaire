    private void inicializarFormulario() {

        this.limpiarFormulario();

        misDocumentos = miController.buscarTiposDeDocumentoDisponibles();

        if (misDocumentos != null && !misDocumentos.isEmpty())
        {
            DefaultListModel lista = new DefaultListModel();

            for (Iterator<DtoTipoDeDocumento> it = misDocumentos.iterator(); it.hasNext();)
            {
                DtoTipoDeDocumento miDto = it.next();

                lista.addElement(miDto.getNombre());
            }

            this.listaDocumentosDisponibles.setModel(lista);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "No existen Tipos de Documento registrados.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            salir();
        }
    }

