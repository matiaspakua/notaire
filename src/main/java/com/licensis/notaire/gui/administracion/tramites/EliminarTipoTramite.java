    private void botonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSeleccionarActionPerformed
        // TODO add your handling code here:

        if (listaTramitesDisponibles.getSelectedValue() != null)
        {

            limpiarGrilla();
            botonAceptar.setEnabled(true);
            campoObservaciones.setEnabled(true);

            String seleccionado = listaTramitesDisponibles.getSelectedValue().toString();

            for (Iterator<DtoTipoDeTramite> it = tramitesDisponibles.iterator(); it.hasNext();)
            {
                DtoTipoDeTramite dtoTipoDeTramite = it.next();

                // Completo los datos en los componentes

                if (dtoTipoDeTramite.getNombre().equals(seleccionado))
                {


                    miDtoSeleccionado = dtoTipoDeTramite;

                    campoNombreTramite.setText(dtoTipoDeTramite.getNombre());
                    checkBoxSeArchiva.setSelected(dtoTipoDeTramite.isSeArchiva());
                    checkRequiereInscripcion.setSelected(dtoTipoDeTramite.isSeInscribe());
                    checkBoxInmueble.setSelected(dtoTipoDeTramite.getAsociaInmuebles());
                    campoObservaciones.setText(dtoTipoDeTramite.getObservaciones());

                    // Obtengo las plantillas de tramite asociadas al tramite
                    plantillas = miController.obtenerPlantillasTramite(dtoTipoDeTramite);

                    //Obtengo los documentos disponibles
                    documentosDisponibles = miController.buscarTiposDeDocumentoDisponibles();

                    if (documentosDisponibles != null && !documentosDisponibles.isEmpty())
                    {
                        DtoTipoDeDocumento miDtoTipoDeDocumento;
                        String nombreDocumento;

                        for (int i = 0; i < documentosDisponibles.size(); i++)
                        {

                            miDtoTipoDeDocumento = documentosDisponibles.get(i);

                            nombreDocumento = miDtoTipoDeDocumento.getNombre();

                            Boolean flag = false;

                            if (plantillas != null && !plantillas.isEmpty())
                            {
                                for (int j = 0; j < plantillas.size(); j++)
                                {
                                    DtoPlantillaTramite dtoPlantillaTramite = plantillas.get(j);

                                    // Si el Tip ode Documento de la lista esta asociado al Tip ode Tramite, le pongo en check en true.
                                    if (dtoPlantillaTramite.getTiposDeDocumento().getNombre().equals(miDtoTipoDeDocumento.getNombre()))
                                    {
                                        flag = true;
                                    }
                                }
                            }

                            if (flag == true)
                            {
                                Object[] datos =
                                {
                                    nombreDocumento,
                                    Boolean.TRUE
                                };
                                ((DefaultTableModel) grillaDocumentosNecesarios.getModel()).addRow(datos);
                            }
                            else
                            {
                                Object[] datos =
                                {
                                    nombreDocumento,
                                    Boolean.FALSE
                                };
                                ((DefaultTableModel) grillaDocumentosNecesarios.getModel()).addRow(datos);
                            }
                        }
                    }
                    else
                    {
                        grillaDocumentosNecesarios.setEnabled(false);
                    }

                    break;
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Tipo de Tramite.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botonSeleccionarActionPerformed

