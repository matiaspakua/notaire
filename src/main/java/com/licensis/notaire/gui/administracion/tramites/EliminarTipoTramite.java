    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        // TODO add your handling code here:

        if (miDtoSeleccionado != null)
        {

            int respuesta = JOptionPane.showConfirmDialog(this, "¿Esta seguro que desea eliminar el Tipo de Tramite " + miDtoSeleccionado.getNombre() + " ?", "CONFIRMACION", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (respuesta == JOptionPane.YES_OPTION)
            {
                Boolean eliminado = false;

                miDtoSeleccionado.setHabilitado(false);

                try
                {
                    eliminado = miController.eliminarTipoDeTramite(miDtoSeleccionado);

                    if (eliminado)
                    {
                        JOptionPane.showMessageDialog(this, "Se ha eliminado el Tipo de Tramite.", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                        limpiarFormulario();
                        inicializarFormulario();
                    }
                }
                catch (ClassModifiedException ex)
                {
                    JOptionPane.showMessageDialog(this, "El Tipo de Tramite ha sido recientemente modificado por otro usuario.", "ADEVERTENCIA", JOptionPane.WARNING_MESSAGE);
                }
                catch (ClassEliminatedException ex)
                {
                    JOptionPane.showMessageDialog(this, "El Tipo de Tramite ha sido recientemente eliminado por otro usuario.", "ADEVERTENCIA", JOptionPane.WARNING_MESSAGE);
                }
                finally
                {
                    limpiarFormulario();
                    inicializarFormulario();
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Debe seleccioanr un Tipo de Tramite.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

