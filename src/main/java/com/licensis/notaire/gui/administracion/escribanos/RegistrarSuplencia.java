    public void cargarGrilla() {
        try
        {
            miListaDtoEscribanos = miControllerNegocio.obtenerListaEscribanosDisponibles();

            if (miListaDtoEscribanos.isEmpty())
            {
                JOptionPane.showMessageDialog(this, "No hay escribanos registrados", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
            else
            {
                for (Iterator<DtoPersona> it = miListaDtoEscribanos.iterator(); it.hasNext();)
                {
                    DtoPersona dtoEscribano = it.next();

                    Object[] datos =
                    {
                        dtoEscribano.getNombre() + " " + dtoEscribano.getApellido(),
                        dtoEscribano.getRegistroEscribano()
                    };

                    ((DefaultTableModel) grillaEscribanosDisponibles.getModel()).addRow(datos);
                }
            }
        }
        catch (NullPointerException e)
        {
            JOptionPane.showMessageDialog(this, "No existen escribanos registrados", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
