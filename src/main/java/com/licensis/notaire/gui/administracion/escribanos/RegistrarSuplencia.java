    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAceptarActionPerformed
    {//GEN-HEADEREND:event_botonAceptarActionPerformed

        Date fechaDesde = selectorFechaDesde.getDate();
        Date fechaHasta = selectorFechaHasta.getDate();

        DtoSuplencia nuevaSuplencia = new DtoSuplencia();
        nuevaSuplencia.setFechaInicio(fechaDesde);
        nuevaSuplencia.setFechaFin(fechaHasta);
        nuevaSuplencia.setObservaciones(campoObservaciones.getText());
        nuevaSuplencia.setPersonasByFkIdSuplente(dtoEscribanoSuplente);
        nuevaSuplencia.setPersonasByFkIdSuplantado(dtoEscribanoSuplantado);

        if (nuevaSuplencia.isValido())
        {
            if (miControllerNegocio.registrarSuplenciaEscribano(nuevaSuplencia) == Boolean.TRUE)
            {
                JOptionPane.showMessageDialog(this, "<HTML>Se ha registrado una suplencia de Escribano:"
                        + "<BR> " + dtoEscribanoSuplantado.getNombre() + " " + dtoEscribanoSuplantado.getApellido()
                        + " por " + dtoEscribanoSuplente.getNombre() + " " + dtoEscribanoSuplente.getApellido()
                        + "</HTML>");
                salir();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Error al registrar suplencia", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Alguno de los datos ingresados no es valido", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_botonAceptarActionPerformed

