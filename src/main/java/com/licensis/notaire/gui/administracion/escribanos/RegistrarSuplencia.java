    private void botonSeleccionarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonSeleccionarActionPerformed
    {//GEN-HEADEREND:event_botonSeleccionarActionPerformed

        if (this.cantidadSelecciones == CAMPO_ESCRIBANO_SUPLENTE)
        {
            this.labelMensajeSuplencia.setText("Favor seleccionar al escribano suplente:");
        }
        else if (this.cantidadSelecciones == CAMPO_ESCRIBANO_SUPLANTADO)
        {
            this.labelMensajeSuplencia.setText("Favor seleccionar el Escribano Suplantado:");
        }

        TableModel miGrilla = grillaEscribanosDisponibles.getModel();
        int filaGrilla = grillaEscribanosDisponibles.getSelectedRow();
        filaGrilla = grillaEscribanosDisponibles.convertRowIndexToModel(filaGrilla);

        try
        {
            int registroEscribano = (int) (grillaEscribanosDisponibles.getModel()).getValueAt(filaGrilla, 1);

            for (Iterator<DtoPersona> it = miListaDtoEscribanos.iterator(); it.hasNext();)
            {
                DtoPersona dtoEscribano = it.next();

                if (dtoEscribano.getRegistroEscribano() == registroEscribano)
                {

                    if (this.cantidadSelecciones == CAMPO_ESCRIBANO_SUPLANTADO)
                    {
                        this.campoEscribanoSuplantado.setText(dtoEscribano.getNombre()
                                + " "
                                + dtoEscribano.getApellido());
                        this.cantidadSelecciones = CAMPO_ESCRIBANO_SUPLENTE;
                        this.labelMensajeSuplencia.setText("Favor seleccionar el Escribano Suplente:");
                        this.dtoEscribanoSuplantado = dtoEscribano;
                    }
                    else
                    {
                        if (this.dtoEscribanoSuplantado.getRegistroEscribano() == dtoEscribano.getRegistroEscribano())
                        {
                            JOptionPane.showMessageDialog(this, "El escribano suplente y suplantado, son inguales. Favor seleccionar otro", "Advertencia", JOptionPane.WARNING_MESSAGE);
                        }
                        else
                        {
                            this.campoEscribanoSuplente.setText(dtoEscribano.getNombre()
                                    + " "
                                    + dtoEscribano.getApellido());
                            //  volvemos a cero.
                            this.cantidadSelecciones = CAMPO_ESCRIBANO_SUPLANTADO;
                            this.labelMensajeSuplencia.setText("");
                            this.dtoEscribanoSuplente = dtoEscribano;
                        }
                    }
                    break;
                }
            }


        }
        catch (ArrayIndexOutOfBoundsException e)
        {
            JOptionPane.showMessageDialog(this, "No ha seleccionado un elemento valido o la lista esta vacia", "Error", JOptionPane.ERROR_MESSAGE);
        }
        catch (NumberFormatException e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_botonSeleccionarActionPerformed

