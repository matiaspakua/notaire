    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAceptarActionPerformed
    {//GEN-HEADEREND:event_botonAceptarActionPerformed

        String msj = AdministradorValidaciones.getInstancia().validarModificarConcepto(this);

        if ("".equals(msj))
        {

            //dtoConceptoSeleccionado.setNombre(this.campoNuevoNombre.getText());
            dtoConceptoSeleccionado.setValor(Float.valueOf(this.campoNuevoValor.getText()));


            if (checkBoxFijo.isSelected())
            {
                dtoConceptoSeleccionado.setFijo(true);
                dtoConceptoSeleccionado.setPorcentaje(0);
            }
            else
            {
                dtoConceptoSeleccionado.setFijo(false);
                dtoConceptoSeleccionado.setPorcentaje(Integer.valueOf(this.selectorNuevoPorcentaje.getValue().toString()));
            }

            if (dtoConceptoSeleccionado.isValido())
            {
                try
                {
                    Boolean modificado;

                    modificado = miControllerNegocio.modificarConcepto(dtoConceptoSeleccionado);

                    if (modificado == Boolean.TRUE)
                    {
                        JOptionPane.showMessageDialog(this, "Se ha modificado un concepto correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);

                        this.cargarListaConceptos();
                        this.listaConceptosDisponibles.updateUI();
                        this.limpiarFormulario();
                    }
                }
                catch (PreexistingEntityException ex)
                {
                    JOptionPane.showMessageDialog(this, "El nombre del concepto ya se encuentra registrado", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }
                catch (ClassEliminatedException ex)
                {
                    JOptionPane.showMessageDialog(this, "El concepto ha sido recientemente eliminado por otro usuario.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                    this.limpiarFormulario();
                    this.cargarListaConceptos();
                }
                catch (ClassModifiedException e)
                {
                    JOptionPane.showMessageDialog(this, "El concepto ha sido recientemente modificado por otro usuario.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                    this.limpiarFormulario();
                    this.cargarListaConceptos();
                }
                catch (NonexistentEntityException | IllegalOrphanException ex)
                {
                    JOptionPane.showMessageDialog(this, "Error grave, Accion cancelada", "Error", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(ModificarConcepto.class.getName()).log(Level.SEVERE, null, ex);
                    this.salir();
                }
                finally
                {
                    limpiarFormulario();
                    cargarListaConceptos();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Alguno de los valores ingresados nos es valido.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }

        }
        else
        {
            JOptionPane.showMessageDialog(this, msj);
        }


    }//GEN-LAST:event_botonAceptarActionPerformed

