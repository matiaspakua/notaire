    private void botonSeleccionarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonSeleccionarActionPerformed
    {//GEN-HEADEREND:event_botonSeleccionarActionPerformed

        Integer nroSeleccionado = listaConceptosDisponibles.getAnchorSelectionIndex();

        if (nroSeleccionado == -1)
        {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Concepto", "Advertencia", JOptionPane.WARNING_MESSAGE);
            this.disableCamposConcepto();
        }
        else if (this.listaConceptosDisponibles.getModel().getSize() > 0)
        {
            this.enableCamposConcepto();
            String nombreConcepto = this.listaConceptosDisponibles.getSelectedValue().toString();
            cargarConcepto(nombreConcepto);

        }
    }//GEN-LAST:event_botonSeleccionarActionPerformed

    private void campoNuevoValorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoNuevoValorKeyReleased
        String valor = this.campoNuevoValor.getText();

        if (AdministradorValidaciones.getInstancia().validarNumeroFloat(valor))
        {
            Float numeroFloat = Float.parseFloat(valor);
            if (numeroFloat < 0)
            {
                labelAdvertencia.setText("El valor no puede ser negativo");
                this.campoNuevoValor.setText("");
            }
            else
            {
                labelAdvertencia.setText("");
            }
        }

    }//GEN-LAST:event_campoNuevoValorKeyReleased

