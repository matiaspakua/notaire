    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed

        Boolean vacio = AdministradorValidaciones.getInstancia().validarCampoVacio(campoNombreTramite.getText());

        if (vacio)
        {
            JOptionPane.showMessageDialog(this, "Complete el nombre del nuevo Tipo de Tramite.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            DtoTipoDeTramite miDtoTipoDeTramite = new DtoTipoDeTramite();

            miDtoTipoDeTramite.setNombre(campoNombreTramite.getText());

            if (checkRequiereInscripcion.isSelected())
            {
                miDtoTipoDeTramite.setSeInscribe(true);
            }
            else
            {
                miDtoTipoDeTramite.setSeInscribe(false);
            }

            if (checkBoxSeArchiva.isSelected())
            {
                miDtoTipoDeTramite.setSeArchiva(true);
            }
            else
            {
                miDtoTipoDeTramite.setSeArchiva(false);
            }

            if (checkBoxInmueble.isSelected())
            {
                miDtoTipoDeTramite.setAsociaInmuebles(true);
            }
            else
            {
                miDtoTipoDeTramite.setAsociaInmuebles(false);
            }

            if (!campoObservaciones.getText().isEmpty())
            {
                miDtoTipoDeTramite.setObservaciones(campoObservaciones.getText());
            }

            ArrayList<DtoTipoDeDocumento> documentos = null;

            if (documentosDisponibles != null && !documentosDisponibles.isEmpty())
            {
                documentos = obtenerDocumentosSeleccionados();
            }

            miDtoTipoDeTramite.setHabilitado(true);

            Boolean creado = false;

            if (!documentos.isEmpty())
            {
                try
                {
                    creado = miController.darAltaTipoDeTramite(miDtoTipoDeTramite, documentos);

                    if (creado)
                    {
                        JOptionPane.showMessageDialog(this, "Se ha dado de alta el tipo de tramite.", "CONFIRMACION", JOptionPane.INFORMATION_MESSAGE);
                        limpiarFormulario();
                    }
                }
                catch (PreexistingEntityException ex)
                {
                    JOptionPane.showMessageDialog(this, "El tipo de tramite ya se encuentra registrado", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);                    
                    this.inicializarFormulario();
                }
                catch (ClassModifiedException ex)
                {
                    JOptionPane.showMessageDialog(this, "<HTML>El tipo de tramite ya existe, <BR>y ha sido recientemente modificado por otro usuario.</HTML>", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                    this.inicializarFormulario();
                }
                catch (ClassEliminatedException ex)
                {
                    JOptionPane.showMessageDialog(this, "<HTML>El tipo de tramite ya existe, <BR>y ha sido recientemente eliminado por otro usuario.</HTML>", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                    this.inicializarFormulario();
                }
                catch (IllegalOrphanException ex)
                {
                    JOptionPane.showMessageDialog(this, "Error grave, Accion cancelada", "Error", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(IngresarTipoTramite.class.getName()).log(Level.SEVERE, null, ex);
                    this.salir();
                }
                catch (NonexistentEntityException ex)
                {
                    JOptionPane.showMessageDialog(this, "Error grave, Accion cancelada", "Error", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(IngresarTipoTramite.class.getName()).log(Level.SEVERE, null, ex);
                    this.salir();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Debe seleccionar al menos un Documento a presentar.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }
        }

    }//GEN-LAST:event_botonAceptarActionPerformed
