    private ArrayList<DtoTipoDeDocumento> obtenerDocumentosSeleccionados() {
        TableModel miGrilla = grillaDocumentosNecesarios.getModel();
        int filas = miGrilla.getRowCount();
        int columnas = miGrilla.getColumnCount();
        Boolean flag = true;
        ArrayList<DtoTipoDeDocumento> documentosSeleccionados = new ArrayList<>();

        for (int i = 0; i < filas; i++)
        {
            flag = (Boolean) miGrilla.getValueAt(i, 1);

            if (flag == true)
            {
                for (int j = 0; j < documentosDisponibles.size(); j++)
                {
                    DtoTipoDeDocumento dtoTipoDeDocumento = documentosDisponibles.get(j);

                    if (((String) miGrilla.getValueAt(i, 0)).equals(dtoTipoDeDocumento.getNombre()))
                    {
                        documentosSeleccionados.add(dtoTipoDeDocumento);
                    }
                }
            }
        }

        return documentosSeleccionados;
    }

