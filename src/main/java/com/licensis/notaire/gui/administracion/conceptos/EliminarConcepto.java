    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAceptarActionPerformed
    {//GEN-HEADEREND:event_botonAceptarActionPerformed
        if (!nombreConceptoSeleccionado.isEmpty())
        {
            for (Iterator<DtoConcepto> it = miListaConceptos.iterator(); it.hasNext();)
            {
                DtoConcepto dtoConcepto = it.next();

                if (dtoConcepto.getNombre().equals(nombreConceptoSeleccionado))
                {
                    try
                    {
                        dtoConceptoSeleccionado = dtoConcepto;

                        dtoConceptoSeleccionado.setHabilitado(false);

                        Boolean resultado = miControllerNegocio.eliminarConcepto(dtoConceptoSeleccionado);

                        if (resultado == true)
                        {
                            JOptionPane.showMessageDialog(this, "Se ha eliminado un concepto correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                            this.cargarListaConceptos();
                        }
                        else
                        {
                        }

                    }
                    catch (ClassModifiedException ex)
                    {
                        JOptionPane.showMessageDialog(this, "El concepto ha sido recientemente modificado por otro usuario.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                        this.cargarListaConceptos();

                    }
                    catch (ClassEliminatedException ex)
                    {
                        JOptionPane.showMessageDialog(this, "El concepto ha sido recientemente eliminado por otro usuario.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                        this.cargarListaConceptos();
                    }
                    catch (NonexistentEntityException | IllegalOrphanException ex)
                    {
                        JOptionPane.showMessageDialog(this, "Error grave, Accion cancelada", "Error", JOptionPane.ERROR_MESSAGE);
                        Logger.getLogger(EliminarConcepto.class.getName()).log(Level.SEVERE, null, ex);
                        this.salir();
                    }
                    finally
                    {

                        DefaultListModel newModel = new DefaultListModel();
                        listaConceptosDisponibles.setModel(newModel);

                        cargarListaConceptos();

                        break;
                    }
                }
            }

        }
        else
        {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Concepto", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_botonAceptarActionPerformed

