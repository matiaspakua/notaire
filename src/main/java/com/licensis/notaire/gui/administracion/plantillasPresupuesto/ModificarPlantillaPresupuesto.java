    private void cargarConceptosDisponibles() {

        limpiarGrilla();
        if (miDtoSeleccionado != null)
        {
            // Obtengo las plantillas de presupuesto asociadas al tramite
            plantillas = miController.obtenerPlantillasPresupuesto(miDtoSeleccionado);

            if (plantillas != null && !plantillas.isEmpty()) //Obtengo los conceptos disponibles
            {
                conceptosDisponibles = miController.obtenerListaConceptosDisponibles();

                if (conceptosDisponibles != null && !conceptosDisponibles.isEmpty())
                {
                    DtoConcepto miDtoConcepto;
                    String nombreConcepto;

                    for (int i = 0; i < conceptosDisponibles.size(); i++)
                    {

                        miDtoConcepto = conceptosDisponibles.get(i);

                        nombreConcepto = miDtoConcepto.getNombre();

                        Boolean flag = false;

                        for (int j = 0; j < plantillas.size(); j++)
                        {
                            DtoPlantillaPresupuesto dtoPlantillaPresupuesto = plantillas.get(j);

                            if (dtoPlantillaPresupuesto.getConceptos().getNombre().equals(miDtoConcepto.getNombre()))
                            {
                                flag = true;
                            }
                        }

                        if (flag == true)
                        {
                            Object[] datos =
                            {
                                nombreConcepto,
                                Boolean.TRUE
                            };
                            ((DefaultTableModel) grillaConceptosDisponibles.getModel()).addRow(datos);
                        }
                        else
                        {
                            Object[] datos =
                            {
                                nombreConcepto,
                                Boolean.FALSE
                            };
                            ((DefaultTableModel) grillaConceptosDisponibles.getModel()).addRow(datos);
                        }
                    }
                }
                else
                {
                    grillaConceptosDisponibles.setEnabled(false);
                }
            }
        }
    }

    private ArrayList<DtoConcepto> obtenerConceptosSeleccionados() {

        TableModel miGrilla = grillaConceptosDisponibles.getModel();
        int filas = miGrilla.getRowCount();
        int columnas = miGrilla.getColumnCount();
        Boolean flag = true;
        ArrayList<DtoConcepto> conceptosSeleccionados = new ArrayList<>();

        for (int i = 0; i < filas; i++)
        {
            flag = (Boolean) miGrilla.getValueAt(i, 1);

            if (flag == true)
            {
                for (int j = 0; j < conceptosDisponibles.size(); j++)
                {
                    DtoConcepto dtoConcepto = conceptosDisponibles.get(j);

                    if (((String) miGrilla.getValueAt(i, 0)).equals(dtoConcepto.getNombre()))
                    {
                        conceptosSeleccionados.add(dtoConcepto);
                    }
                }
            }
        }

        return conceptosSeleccionados;
    }

    private void botonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSeleccionarActionPerformed
        // TODO add your handling code here:
        miDtoSeleccionado = null;
        if (listaTramitesDisponibles.getSelectedValue() != null)
        {
            String seleccionado = listaTramitesDisponibles.getSelectedValue().toString();

            for (Iterator<DtoTipoDeTramite> it = tramitesDisponibles.iterator(); it.hasNext();)
            {
                DtoTipoDeTramite dtoTipoDeTramite = it.next();

                // Completo los datos en los componentes

                if (dtoTipoDeTramite.getNombre().equals(seleccionado))
                {

                    miDtoSeleccionado = dtoTipoDeTramite;

                    cargarConceptosDisponibles();

                    break;
                }
            }
            
            botonAceptar.setEnabled(true);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Tipo de Tramite.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botonSeleccionarActionPerformed
