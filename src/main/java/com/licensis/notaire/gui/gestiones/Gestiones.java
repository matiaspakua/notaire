    private void botonAdministrarEscriturasActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAdministrarEscriturasActionPerformed
    {//GEN-HEADEREND:event_botonAdministrarEscriturasActionPerformed
        if (Escrituras.getEstadoFormulario() == Boolean.FALSE)
        {
            Principal.cargarFormulario(Escrituras.getInstancia());
            Principal.setVentanasActivas(Escrituras.getSubmenuEscrituras());
        }
        else
        {
            for (int i = 0; i < Principal.AreaTrabajo.getAllFrames().length; i++)
            {
                if (Principal.AreaTrabajo.getAllFrames()[i].equals(Escrituras.getInstancia()))
                {
                    Principal.cargarFormulario(Escrituras.getInstancia());
                    Principal.setVentanasActivas(Escrituras.getSubmenuEscrituras());
                }
            }
        }
    }//GEN-LAST:event_botonAdministrarEscriturasActionPerformed

