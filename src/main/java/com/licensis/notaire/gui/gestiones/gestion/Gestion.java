    private void botonArchivarGestionActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonArchivarGestionActionPerformed
    {//GEN-HEADEREND:event_botonArchivarGestionActionPerformed
        ArchivarGestion formArchivarGestion = ArchivarGestion.getInstancia();

        boolean flag = formArchivarGestion.cargarGrillaGestiones();
        if (!flag)
        {
            JOptionPane.showMessageDialog(this, "No hay gestion para ser archivadas");
        }
        else
        {
            Principal.cargarFormulario(formArchivarGestion);
            Principal.setVentanasActivas(ArchivarGestion.getVentanaArchivarGestion());
        }


    }//GEN-LAST:event_botonArchivarGestionActionPerformed

